<h1>Hello, world!</h1>
<p>Welcome to your new single-page application, built with:</p>
<ul>
    <li>
        <a href="https://get.asp.net/">ASP.NET Core</a> and
        <a href="https://msdn.microsoft.com/en-us/library/67ef8sbd.aspx">C#</a> for
        cross-platform server-side code
    </li>
    <li>
        <a href="https://svelte.dev/">Svelte</a> and
        <a href="https://www.typescriptlang.org/">TypeScript</a> for client-side code
    </li>
    <li>
        <a href="https://getbootstrap.com/">Bootstrap</a> for layout and styling
    </li>
</ul>
<p>To help you get started, we've also set up:</p>
<ul>
    <li>
        <strong>Client-side navigation</strong>. For example, click
        <em>Counter</em>
        then <em>Back</em> to return here.
    </li>
    <li>
        <strong>SvelteKit integration</strong>. In development mode,
        <a href="https://vitejs.dev/">Vite</a>
        from <code>svelte-kit dev</code> runs in the background automatically, so
        your client-side resources are dynamically built on demand and the page refreshes
        when you modify any file.
    </li>
    <li>
        <strong>Efficient production builds</strong>. In production mode,
        development-time features are disabled, and your
        <code>dotnet publish</code>
        configuration automatically invokes <code>svelte-kit build</code>
        to produce minified, ahead-of-time compiled JavaScript files.
    </li>
</ul>
<p>
    The <code>ClientApp</code> subdirectory is a standard Svelte application
    based on the <code>create-svelte</code> template. If you open a
    command prompt in that directory, you can run <code>npm</code> commands such
    as <code>npm test</code> or <code>npm install</code>.
</p>
